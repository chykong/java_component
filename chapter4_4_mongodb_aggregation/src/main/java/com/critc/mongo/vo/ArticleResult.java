package com.critc.mongo.vo;

/**
 * 统计结果
 */
public class ArticleResult {
	private String name;
	private Integer count;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
}
